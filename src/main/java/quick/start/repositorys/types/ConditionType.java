package quick.start.repositorys.types;

/**
 * 条件类型
 *
 * @author yuanweiquan
 */
public enum ConditionType {
     /**
      * 等于
      */
     EQUAL,
     /**
      * 不等于
      */
     NOT_EQUAL,
     /**
      * 小于
      */
     LESS_THEN,
     /**
      * 小于等于
      */
     LESS_THEN_OR_EQUAL,
     /**
      * 大于
      */
     GREATER_THEN,
     /**
      * 大于等于
      */
     GREATER_THEN_OR_EQUAL,
     /**
      * in
      */
     IN,
     /**
      * like
      */
     LIKE
}
